#!/usr/bin/env node
// Generates file descriptor protos from .proto files for the Canopy Circle plugin

const protobuf = require('protobufjs');
const descriptor = require('protobufjs/ext/descriptor');
const fs = require('fs');
const path = require('path');

async function main() {
    const protoDir = path.join(__dirname, '..', 'proto');
    const outFile = path.join(__dirname, '..', 'src', 'proto', 'descriptors.ts');

    const protoFiles = [
        'account.proto',
        'event.proto',
        'tx.proto',
        'social.proto',
        'plugin.proto',
    ];

    const root = new protobuf.Root();
    root.resolvePath = (origin, target) => {
        if (target.startsWith('google/protobuf/')) return target;
        return path.join(protoDir, path.basename(target));
    };

    for (const protoFile of protoFiles) {
        await root.load(path.join(protoDir, protoFile), { keepCase: true });
    }

    const descriptorSet = root.toDescriptor('proto3');
    const fileDescriptorProtos = [];

    for (const file of descriptorSet.file) {
        if (file.name && (file.name.includes('google') || file.name === 'google_protobuf.proto')) {
            file.name = 'google/protobuf/any.proto';
            const encoded = descriptor.FileDescriptorProto.encode(file).finish();
            fileDescriptorProtos.push(Buffer.from(encoded).toString('base64'));
            console.log('Added google/protobuf/any.proto descriptor');
            break;
        }
    }

    const fileTypes = {};
    for (const protoFile of protoFiles) {
        const content = fs.readFileSync(path.join(protoDir, protoFile), 'utf8');
        const messageMatches = content.matchAll(/^message\s+(\w+)\s*\{/gm);
        const enumMatches = content.matchAll(/^enum\s+(\w+)\s*\{/gm);
        fileTypes[protoFile] = {
            messages: [...messageMatches].map(m => m[1]),
            enums: [...enumMatches].map(m => m[1]),
        };
    }

    for (const protoFile of protoFiles) {
        let typesDescriptor = null;
        for (const file of descriptorSet.file) {
            if (file.name === 'types.proto' || (file.messageType && file.messageType.length > 0)) {
                typesDescriptor = file;
                break;
            }
        }

        if (!typesDescriptor) {
            console.warn(`Could not find descriptor for ${protoFile}`);
            continue;
        }

        const cloned = Object.assign({}, typesDescriptor);
        cloned.name = protoFile;

        const info = fileTypes[protoFile];
        if (info) {
            cloned.messageType = (typesDescriptor.messageType || []).filter(
                m => info.messages.includes(m.name)
            );
            cloned.enumType = (typesDescriptor.enumType || []).filter(
                e => info.enums.includes(e.name)
            );
        }

        try {
            const encoded = descriptor.FileDescriptorProto.encode(cloned).finish();
            fileDescriptorProtos.push(Buffer.from(encoded).toString('base64'));
            console.log(`Added descriptor for ${protoFile} (${cloned.messageType?.length || 0} messages)`);
        } catch (e) {
            console.warn(`Could not encode descriptor for ${protoFile}: ${e.message}`);
        }
    }

    const output = `// Auto-generated by scripts/generate-descriptors.cjs — DO NOT EDIT
// Run: npm run build:descriptors

export const fileDescriptorProtos: Uint8Array[] = [
${fileDescriptorProtos.map(b => `    Buffer.from('${b}', 'base64')`).join(',\n')}
];
`;

    fs.writeFileSync(outFile, output);
    console.log(`\nWrote ${fileDescriptorProtos.length} descriptors to ${outFile}`);
}

main().catch(e => { console.error(e); process.exit(1); });
